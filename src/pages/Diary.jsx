const formatDate = (dateString) => {
  const options = { year: 'numeric', month: 'short', day: '2-digit' };
  const date = new Date(dateString);
  return date.toLocaleDateString('en-US', options).replace(/,/, '').replace(/(\w+) (\d+) (\d+)/, '$1, $2 $3');
};

